package org.primary.operator;

public class CompareTest {
    public static void main(String[] args) {
        // 关系运算符返回结果为：true 和 false
        System.out.println(5>7); // false
        System.out.println(5<7); // true
        System.out.println(5>=7); // false
        System.out.println(5<=7); // true
        System.out.println(5==7); // false
        System.out.println(5!=7); // true
    }
}
